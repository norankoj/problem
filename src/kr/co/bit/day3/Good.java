package kr.co.bit.day3;

public class Good {
	
	private String code;
	private String mail;
	private String korea;
	private String english;
	private String math;
	private String science;
	private String history;
	private String sum;
	private String teacher;
	private String achievement;
	private String area;
	
	
	
	public Good() {
	}

	public Good(String code, String mail, String korea, String english, String math, String science, String history,
			String sum, String teacher, String achievement, String area) {
		this.code = code;
		this.mail = mail;
		this.korea = korea;
		this.english = english;
		this.math = math;
		this.science = science;
		this.history = history;
		this.sum = sum;
		this.teacher = teacher;
		this.achievement = achievement;
		this.area = area;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMail() {
		return mail;
	}

	public void setMail(String mail) {
		this.mail = mail;
	}

	public String getKorea() {
		return korea;
	}

	public void setKorea(String korea) {
		this.korea = korea;
	}

	public String getEnglish() {
		return english;
	}

	public void setEnglish(String english) {
		this.english = english;
	}

	public String getMath() {
		return math;
	}

	public void setMath(String math) {
		this.math = math;
	}

	public String getScience() {
		return science;
	}

	public void setScience(String science) {
		this.science = science;
	}

	public String getHistory() {
		return history;
	}

	public void setHistory(String history) {
		this.history = history;
	}

	public String getSum() {
		return sum;
	}

	public void setSum(String sum) {
		this.sum = sum;
	}

	public String getTeacher() {
		return teacher;
	}

	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}

	public String getAchievement() {
		return achievement;
	}

	public void setAchievement(String achievement) {
		this.achievement = achievement;
	}

	public String getArea() {
		return area;
	}

	public void setArea(String area) {
		this.area = area;
	}
	
	
	
	
	
	
	

}
